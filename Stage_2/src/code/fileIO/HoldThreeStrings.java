package code.fileIO;

public class HoldThreeStrings {
	public String line1;
	public String line2;
	public String line3;
	
	/**
	 * Wraper class for 3 strings
	 * 
	 * @param a
	 * @param b
	 * @param c
	 */
	public HoldThreeStrings(String a, String b, String c){
		line1 = a;
		line2 = b;
		line3 = c;
	}
}
